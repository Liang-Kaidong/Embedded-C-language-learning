#include <stdio.h>

int main()
{
    int a = 65, b = 0;
    /* a的16进制为0x41 */
    printf("%#x %d\n", a, a);	

    /* 
    * b = a / 16 
    * b对a进行16位进制运算取整,商4 
    * a左移4,得到后四位的16进制为4
    */
    b = a >> 4;
    printf("%#x %d\n", b, b);

    /* 
    * b = a % 16;
    * b对a进行16位进制运算求余,余1 
    * 得到的余数即为后4位,
    * 前四位清0,后四位保留. 
    * 用&运算,前四位0000,后四位1111,
    * 00001111在16进制位为f.
    */
    b = a & 0xf;
    printf("%#x %d\n", b, b);	
    return 0;   
}





